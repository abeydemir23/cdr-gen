package com.cdr.gen;

public class FuzzifiedCall {

    private long id;
    private String type;
    private String duration;
    private String cost;
    private String fraud;
    private String destPhoneNumber;

    public long getId() {
	return id;
    }

    public void setId(long id) {
	this.id = id;
    }

    public String getType() {
	return type;
    }

    public void setType(String type) {
	this.type = type;
    }

    public String getDuration() {
	return duration;
    }

    public void setDuration(String duration) {
	this.duration = duration;
    }

    public String getCost() {
	return cost;
    }

    public void setCost(String cost) {
	this.cost = cost;
    }

    public String getFraud() {
	return fraud;
    }

    public void setFraud(String fraud) {
	this.fraud = fraud;
    }

    public String getDestPhoneNumber() {
	return destPhoneNumber;
    }

    public void setDestPhoneNumber(String destPhoneNumber) {
	this.destPhoneNumber = destPhoneNumber;
    }

}
